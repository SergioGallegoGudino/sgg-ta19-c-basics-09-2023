class Ejercicio4
{
    static void Main()
    {
        int N = 50;

        int suma = N + 77;
        int resta = suma - 3;
        int multiplicacion = resta * 2;

        Console.WriteLine("Valor inicial: " + N);
        Console.WriteLine("N + 77 = " + suma);
        Console.WriteLine("N - 3 = " + resta);
        Console.WriteLine("N * 2 = " + multiplicacion);
    }
}

